        <div class="row justify-content-center">
          <div class="col-md-8 mb-5">
            <h3 align="center">ESTATUTOS DA ASSOCIAÇÃO OTC</h3>
            <h4 align="center">CAPÍTULO I</h4>
            <h4 align="center">ARTIGO 1.º</h4>
            <h5 align="center">DESIGNAÇÃO, SEDE E DURAÇÃO</h5>
            <p>A Associação tem a denominação de “ASSOCIAÇÃO OTC – OFICINA DE TEATRO DE CONDEIXA”, adiante designada por Associação, tem a sua sede na Avenida Visconde de Alverca, 57 D, na vila de Condeixa-a-Nova, na União das freguesias de Condeixa-a-Velha e Condeixa-a-Nova, concelho de Condeixa-a-Nova, podendo mudar para outro local, por decisão da Assembleia Geral e durará por tempo indeterminado.</p>
            <h4 align="center">ARTIGO 2.º</h4>
            <h5 align="center">CARÁCTER E OBJETIVOS</h5>
            <p>A Associação tem âmbito nacional, é constituída sem fins lucrativos, sem qualquer orientação política ou religiosa. Tem como objeto a divulgação das artes performativas, bem como de outras formas de expressão cultural e recreativa, contribuindo para a educação da comunidade.</p>
            <h4 align="center">ARTIGO 3.º</h4>
            <h5 align="center">RELAÇÕES COM OUTRAS ORGANIZAÇÕES</h5>
            <p>A Associação poderá estabelecer relações com quaisquer organizações nacionais e internacionais com elas acordando formas de cooperação consentâneas com o seu objetivo.</p>
            <h4 align="center">CAPÍTULO II</h4>
            <h4 align="center">ARTIGO 4.º</h4>
            <h5 align="center">RECEITAS E DESPESAS</h5>
            <ol>
              <li>As receitas da associação compreendem:
                <ol type="a">
                  <li>As quotas dos seus associados, cujo valor é aprovado em Assembleia Geral;</li>
                  <li>As doações, subvenções, subsídios e outras receitas que, eventualmente, lhe sejam atribuídos.</li>
                </ol>
              </li>
              <li>
                São despesas da associação as que resultam do exercício das suas atividades em cumprimento dos Estatutos, do Regulamento Geral Interno e das disposições que sejam impostas por lei.
              </li>
            </ol>
            <h4 align="center">CAPÍTULO III</h4>
            <h4 align="center">ARTIGO 5.º</h4>
            <h5 align="center">ASSOCIADOS</h5>
            <ol>
              <li>Podem ser associados da Associação todos os indivíduos, sem limite de idade, que queiram contribuir para o engrandecimento da Associação e que demonstrem ter bom comportamento moral e civil aceitando os deveres/obrigações enquanto associado.</li>
              <li>O Regulamento Geral Interno especificará os direitos e as obrigações dos associados.</li>
              <li>Os associados entram no pleno gozo dos seus direitos após aprovação da sua admissão em reunião de Direção, mediante o pagamento da primeira quota.</li>
              <li>Os associados podem ter a seguinte categoria: fundadores, ativos, não ativos e honorários:
                <ol type="a">
                  <li>Associados fundadores são os aderentes à data de aprovação dos presentes estatutos;</li>
                  <li>Associados ativos são os que participam ativamente nas atividades desenvolvidas pela Associação;</li>
                  <li>Associados não ativos, correspondem aos sócios que não exercem atividade regular no âmbito associativo;</li>
                  <li>Associados honorários são as personalidades e entidades de renome nacional ou internacional cuja ação notável está de acordo com os objetivos da OTC.</li>
                </ol>
              </li>
              <li>A nomeação dos associados honorários é proposta pela Direção da OTC, sendo que a sua designação é da competência da Assembleia Geral.</li>
              <li>Os associados honorários estão isentos de quotas.</li>
              <li>Só poderão ser eleitos para cargos diretivos os associados que tiverem completado a maioridade legal, sendo a capacidade eleitoral - voto - atingida aos dezasseis anos.</li>
            </ol>
            <h4 align="center">CAPÍTULO IV</h4>
            <h4 align="center">ARTIGO 6.º</h4>
            <h5 align="center">ÓRGÃOS</h5>
            <ol>
              <li>São órgãos da Associação:
                <ol type="a">
                  <li>A Assembleia Geral;</li>
                  <li>A Direção;</li>
                  <li>O Conselho Fiscal.</li>
                </ol>
              </li>
            </ol>
            <li>O mandato dos órgãos eleitos da associação é de quatro anos.</li>
            <h4 align="center">ARTIGO 7.º</h4>
            <h5 align="center">ASSEMBLEIA GERAL</h5>
            <ol>
              <li>A Assembleia Geral é constituída por todos os associados em pleno gozo dos seus direitos.</li>
              <li>A Assembleia Geral reúne em sessão ordinária uma vez por ano, e, em sessão extraordinária, por convocação do seu Presidente e ou a pedido da Direção e ou do Conselho Fiscal e ou de vinte por cento dos associados em pleno gozo dos seus direitos.</li>
              <li>A Assembleia Geral será convocada com a antecedência mínima de oito dias, por aviso postal e afixação na sede da associação.</li>
              <li>A convocatória deve definir o local e a agenda da reunião.</li>
              <li>Compete à Assembleia Geral:
                <ol type="a">
                  <li>Discutir e votar o relatório e contas anuais a apresentar pela Direção, após parecer do Conselho Fiscal;</li>
                  <li>Discutir e votar o plano de atividades para o ano seguinte;</li>
                  <li>Eleger e demitir os membros dos órgãos sociais nos termos dos presentes estatutos;</li>
                  <li>Deliberar sobre quaisquer assuntos propostos pelos associados ou órgãos da Associação;</li>
                  <li>Deliberar, por proposta da Direção ou de vinte por cento dos sócios, sobre a alteração dos estatutos e sobre a extinção da Associação, bem como sobre a exclusão de qualquer dos sócios;</li>
                  <li>Fixar a quota anual;</li>
                  <li>Aprovar as atas das reuniões anteriores.</li>
                </ol>
              </li>
              <li>As deliberações são tomadas por maioria dos presentes, salvo nos seguintes casos:
                <ol type="a">
                  <li>Alteração dos estatutos e destituição dos corpos sociais, em que é necessária a maioria de três quartos dos associados presentes;</li>
                  <li>Extinção da Associação, em que é necessária a maioria de três quartos do total dos associados.</li>
                </ol>
              </li>
            </ol>
            <h4 align="center">ARTIGO 8.º</h4>
            <h5 align="center">MESA DA ASSEMBLEIA GERAL</h5>
            <ol>
              <li>A Mesa da Assembleia Geral é constituída pelo presidente, primeiro secretário, segundo secretário, primeiro suplente e segundo suplente.</li>
              <li>Compete ao presidente da mesa:
                <ol type="a">
                  <li>Convocar as Assembleias Gerais;</li>
                  <li>Presidir às reuniões e orientar os trabalhos;</li>
                  <li>Dar posse aos corpos diretivos no prazo de quinze dias após a eleição pela Assembleia Geral;</li>
                  <li>Assinar as atas e legalizar os livros da Assembleia Geral.</li>
                </ol>
              </li>
              <li>Compete aos secretários o expediente da mesa: gravar, transcrever, ler e assinar as atas.</li>
            </ol>
            <h4 align="center">ARTIGO 9.º</h4>
            <h5 align="center">DIREÇÃO</h5>
            <ol>
              <li>A Direção é composta pelo presidente, vice-presidente, secretário, tesoureiro e um vogal, eleitos em Assembleia Geral.</li>
              <li>São atribuições da Direção:
                <ol type="a">
                  <li>Gerir toda a atividade da Associação, dando cumprimento efetivo a todos os objetivos desta;</li>
                  <li>Representar a Associação perante terceiros;</li>
                  <li>Dar cumprimento às deliberações da Assembleia Geral;</li>
                  <li>Elaborar e submeter à Assembleia Geral o plano de atividades e orçamento, bem como o relatório de contas anuais, para aprovação;</li>
                  <li>Propor a demissão de associados à Assembleia Geral, nos termos dos Estatutos;</li>
                  <li>Propor alterações estatutárias.</li>
                </ol>
              </li>
              <li>Compete ao presidente ou a quem o substituir:
                <ol type="a">
                  <li>Presidir às reuniões da Direção;</li>
                  <li>Convocar os membros da Direção para as reuniões;</li>
                  <li>Cumprir e fazer cumprir as deliberações tomadas em reunião de Direção;</li>
                  <li>Gerir financeiramente a Associação, juntamente com o tesoureiro, e assinar, com este, a respetiva documentação;</li>
                  <li>Representar a Associação em juízo e fora dele, podendo substabelecer os poderes em Advogado ou Solicitador, praticando todos os atos necessários à defesa dos interesses da Associação;</li>
                  <li>Na falta ou impedimento do Presidente, este será substituído por um vice-presidente.</li>
                </ol>
              </li>
              <li>Compete ao vice-presidente:
                <ol type="a">
                  <li>Assumir as competências e funções de Presidente, no caso de este não poder exercer as suas funções temporária ou definitivamente e sempre que mandatado pelo Presidente, formal ou informalmente;</li>
                  <li>Auxiliar o Presidente quando convocado;</li>
                  <li>Coordenar as relações com outras associações;</li>
                  <li>Gerir os meios de comunicação da Associação, conforme os objetivos da mesma, dispostos no artigo 2.º dos presentes estatutos.</li>
                </ol>
              </li>
              <li>Compete ao secretário:
                <ol type="a">
                  <li>Preparar e redigir o expediente da secretária, dando-lhe o necessário andamento e proceder ao seu arquivamento;</li>
                  <li>Redigir as atas da Direção;</li>
                  <li>Conferir a caixa, juntamente com o tesoureiro.</li>
                </ol>
              </li>
              <li>Compete ao tesoureiro:
                <ol type="a">
                  <li>Conferir e assinar com o presidente todos os documentos de receita e despesa;</li>
                  <li>Proceder ao pagamento dos encargos/despesas da Associação;</li>
                  <li>Cobrar e arrecadar as receitas, promovendo o seu depósito em contas bancárias da Associação abertas para o efeito.</li>
                </ol>
              </li>
              <li>A Direção reunirá pelo menos duas vezes por semestre e sempre que seja necessário, sendo obrigatória, para deliberação, a presença da maioria dos seus membros.</li>
              <li>As deliberações serão, sempre, tomadas por maioria dos votos dos presentes, tendo, em caso de empate, o presidente voto de qualidade.</li>
            </ol>
            <h4 align="center">ARTIGO 10.º</h4>
            <h5 align="center">CONSELHO FISCAL</h5>
            <ol>
              <li>O Conselho Fiscal é constituído pelo presidente, primeiro secretário e segundo secretário, eleitos em Assembleia Geral.</li>
              <li>Compete ao Conselho Fiscal:
                <ol type="a">
                  <li>Verificar, periodicamente, a regularidade das contas, quer no seu aspeto contabilístico, quer na sua correspondência com a situação real;</li>
                  <li>Solicitar a convocação da Assembleia Geral se se verificar a existência de abusos ou irregularidades graves em matéria de gestão económica e financeira;</li>
                  <li>Emitir parecer prévio do relatório de contas apresentado pela direção.</li>
                </ol>
                <li>O conselho fiscal reúne sempre que o desempenho das suas funções o exigir, no mínimo de uma vez por ano fiscal.</li>
                <li>As deliberações são tomadas por maioria, tendo o presidente voto de qualidade.</li>
            </ol>
            <h4 align="center">ARTIGO 11.º</h4>
            <h5 align="center">ELEIÇÕES</h5>
            <ol>
              <li>A eleição para a Mesa da Assembleia Geral, para o Conselho Fiscal e para a Direção, efetua-se mediante a apresentação de listas até oito dias antes da sessão da Assembleia Geral convocada para o efeito, sendo eleita a lista mais votada.</li>
              <li>Das listas devem constar os três órgãos sociais.</li>
              <li>As eleições serão feitas por meio de voto secreto, presencial e em urna.</li>
              <li>A cada associado corresponde um voto.</li>
              <li>Os membros dos órgãos sociais só cessam funções, com a posse dos seus substitutos, a qual é conferida pela Mesa da Assembleia Geral cessante.</li>
            </ol>
            <h4 align="center">CAPÍTULO V</h4>
            <h4 align="center">ARTIGO 12.º</h4>
            <h5 align="center">FORMA DE OBRIGAR</h5>
            <p>A Associação obriga-se com duas assinaturas dos membros da Direção.</p>
            <h4 align="center">ARTIGO 13.º</h4>
            <h4 align="center">DESTITUIÇÃO</h4>
            <p>Quando as atividades da Direção da Associação se tornem incompatíveis com os seus fins, os associados poderão apresentar à Assembleia Geral uma proposta de destituição, exigindo-se a maioria de três quartos de votos dos associados presentes, em reunião convocada para esse fim.</p>
            <h4 align="center">ARTIGO 14.º</h4>
            <h4 align="center">DISSOLUÇÃO</h4>
            <ol>
              <li>A Associação dissolve-se:<br>Nos casos previstos no artigo cento e oitenta e dois do Código Civil.</li>
              <li>Para decisão definitiva da dissolução, será convocada a Assembleia Geral, a qual, por maioria de votos, deliberará o destino a dar aos bens da Associação.</li>
            </ol>
            <h4 align="center">CAPÍTULO VI</h4>
            <h4 align="center">ARTIGO 15.º</h4>
            <h5 align="center">DISPOSIÇÕES GERAIS</h5>
            <p>No que os presentes estatutos sejam omissos, será regido pela lei em vigor e pelo Regulamento Interno, cuja aprovação e alteração são da competência da Assembleia Geral, sempre em obediência à lei geral.</p>
            
