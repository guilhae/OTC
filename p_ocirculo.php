            No entanto, um conflito muda tudo. <br><br>
            Maryam, uma trabalhadora fabril, encontra uma criança abandonada à porta da fábrica que, sem chorar, sem falar, sem se mover, coloca-a diante de uma difícil escolha em tempos de guerra. <br>
            Tornando-se o epicentro da trama, a personagem de Maryam desafia não apenas as circunstâncias adversas da guerra, mas também as normas estabelecidas pela sociedade, mergulhando nos dilemas morais e éticos que emergem em tempos de conflito e colocando em evidência a complexidade das relações humanas diante das adversidades extremas.<br>
